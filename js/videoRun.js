var figure0 = $(".video0");

[].forEach.call(figure0, function (item) {
        item.addEventListener('mouseover', hoverVideo0, false);
        item.addEventListener('mouseout', hideVideo0, false);
});

function hoverVideo0(e) {  
        $('.thevideo0')[0].play(); 
}

function hideVideo0(e) {
        $('.thevideo0')[0].pause(); 
}


